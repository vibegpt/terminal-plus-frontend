// Helper function to get amenities for a collection
export function getAmenitiesForCollection(collectionSlug: string): string[] {
  return COLLECTION_AMENITIES[collectionSlug as keyof typeof COLLECTION_AMENITIES] || [];
}

// Get collection stats
export function getCollectionStats(collectionSlug: string) {
  const amenities = getAmenitiesForCollection(collectionSlug);
  return {
    totalCount: amenities.length,
    isOptimal: amenities.length >= 6 && amenities.length <= 12,
    needsMore: amenities.length < 6,
    tooMany: amenities.length > 12
  };
}
